public class Evenodd
{
	public static void main(String[] args) {
		int arr[]={2,4,0,56,78,23,0,1,2};
	    
	    for(int i=0;i<9;i++)
	    {
	        if(arr[i]%2==0)
	        {
	            System.out.println(arr[i]);
	        }
	  
	        if(arr[i]%2!=0)
	        {
	            System.out.println(arr[i]);
	        }
	    }
}
}